void emitData(std::ostream& out, std::uint64_t heapCapacityBytes) {
    out << "section .data\n"
        << "    heap_capacity: dq " << heapCapacityBytes << "\n"
        << "    heap_start: dq 0\n"
        << "    heap_pointer: dq 0\n"
        << "    heap_end: dq 0\n"
        << "    ; GC block header flags live in the two high bits of header[0].\n"
        << "    gc_mark_flag: dq 0x8000000000000000\n"
        << "    gc_free_flag: dq 0x4000000000000000\n"
        << "    gc_size_mask: dq 0x3fffffffffffffff\n"
        << "    heap_free_list: dq 0\n"
        << "    gc_stack_top: dq 0\n"
        << "    gc_mark_changed: dq 0\n"
        << "    gc_candidate_source: dq 0\n"
        << "    gc_collections: dq 0\n"
        << "    gc_last_freed_bytes: dq 0\n"
        << "    gc_last_largest_free_block: dq 0\n"
        << "    gc_last_marked_blocks: dq 0\n"
        << "    gc_last_freed_blocks: dq 0\n"
        << "    gc_last_stack_words: dq 0\n"
        << "    gc_last_heap_words: dq 0\n"
        << "    gc_last_stack_candidate_words: dq 0\n"
        << "    gc_last_heap_candidate_words: dq 0\n"
        << "    heap_overflow_message: db \"Nabla runtime error: heap exhausted\", 10\n"
        << "    heap_overflow_message_len: equ $ - heap_overflow_message\n"
        << "    newline: db 10\n"
        << "    align 8\n"
        << "    floatToStringAbsMask: dq 0x7fffffffffffffff\n"
        << "    floatToStringScale: dq 1000000.0\n\n";
    emitRuntimeHelperAllocMetadata(out);
}

void emit(std::ostream& out, std::uint64_t heapCapacityBytes, bool mainAcceptsArguments) {
    emitData(out, heapCapacityBytes);
    emitText(out, mainAcceptsArguments);
}

